package model;

/**
 *
 * @author Admin
 */
public class KhachHang {

    private String ma, ten, gioitinh, sdt, email, diachi, trangthai;

    public KhachHang() {
    }

    public KhachHang(String ma, String ten, String gioitinh, String sdt, String email, String diachi, String trangthai) {
        this.ma = ma;
        this.ten = ten;
        this.gioitinh = gioitinh;
        this.sdt = sdt;
        this.email = email;
        this.diachi = diachi;
        this.trangthai = trangthai;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getGioitinh() {
        return gioitinh;
    }

    public void setGioitinh(String gioitinh) {
        this.gioitinh = gioitinh;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }

    public String getTrangthai() {
        return trangthai;
    }

    public void setTrangthai(String trangthai) {
        this.trangthai = trangthai;
    }

}
