    private void showData(ArrayList<HoaDonResponse> listHDS) {
        model.setRowCount(0);
        for (HoaDonResponse hds : listHDS) {
            Object data[] = {
                hds.getMaHD(),
                hds.getNgayTao(),
                hds.getNgayThanhToan(),
                hds.getThanhTien(),
                hds.getGiamGiaKM(),
                (hds.getHinhThucThanhToan() == 0 ? "Tiền mặt"
                : (hds.getHinhThucThanhToan() == 1 ? "Chuyển khoản" : "Kết hợp")),
                hds.getMaNV(),
                hds.getTenKH(),
                (hds.getTrangThai() == 1 ? "Đã thanh toán" : "Chưa thanh toán")};
            model.addRow(data);
        }
    }

    private void showHDCT(ArrayList<HDCTResponse> listHDCT) {
        model2.setRowCount(0);
        for (HDCTResponse hdct : listHDCT) {
            Object data[] = {
                hdct.getMaSP(),
                hdct.getTenSP(),
                hdct.getHang(),
                hdct.getMau(),
                hdct.getSize(),
                hdct.getSoLuong(),
                hdct.getDonGia(),
                hdct.getTongTien()
            };
            model2.addRow(data);
        }
    }

